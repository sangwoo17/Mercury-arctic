

function c_dot = p3_ode(t,c,k)


c_dot(1,1) = - k(1,1)*c(1,1) + k(3,1)*c(2,1) + k(6,1)*c(4,1); 
c_dot(2,1) = + k(1,1)*c(1,1) - (k(2,1)+k(3,1))*c(2,1) - 2*k(4,1)*c(2,1)*c(2,1) +2*k(5,1)*c(4,1);
c_dot(3,1) = + k(2,1)*c(2,1) + k(6,1)*c(4,1);
c_dot(4,1) = + k(4,1)*c(2,1)*c(2,1) - (k(5,1)+k(6,1))*c(4,1);

end

